    private void cbKhoaThiItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbKhoaThiItemStateChanged
        if(!cbKhoaThi.getSelectedItem().equals("--Chọn khóa thi--")){
            int idkt = Integer.parseInt(String.valueOf(cbKhoaThi.getSelectedItem()).split(":")[0]);
            setDSPT(idkt);

        }else{
            cbDSPT.removeAllItems();
            cbDSPT.addItem("--Danh sách phòng thi--");
        }
        
    }//GEN-LAST:event_cbKhoaThiItemStateChanged

    private void cbDSPTItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbDSPTItemStateChanged
        if(cbDSPT.getSelectedItem()!=null){
            if(!cbDSPT.getSelectedItem().equals("--Danh sách phòng thi--")){
            int idpt = Integer.parseInt(String.valueOf(cbDSPT.getSelectedItem()).split(":")[0]);
            setDSTS(idpt);

        }else{
            model.setRowCount(0);
        }
        }
        
    }//GEN-LAST:event_cbDSPTItemStateChanged

