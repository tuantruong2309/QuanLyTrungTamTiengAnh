    public void setDSTS(int idpt){
        model.setRowCount(0);
        ArrayList<SapXepPhongThi_DTO> list = pb.getDSTS(idpt);
        int i=1;
        for(SapXepPhongThi_DTO s : list){
            model.addRow(new Object[]{i,s.getIdts(),s.getSbd(),s.getHo(),s.getTen()});
            i++;
        }
    }
    public void loadTSChuaCoPT(){
        model2.setRowCount(0);
        ArrayList<ThiSinh_DTO> list = pb.DSThiSinh_ChuaCoPhong();
        for(ThiSinh_DTO ts : list){
            model2.addRow(new Object[]{ts.getIDThiSinh(),ts.getHo()+" "+ts.getTen(),ts.getTrinhDo()});
        }
    }
    public void setDSPT(int idkt){
        cbDSPT.removeAllItems();
        cbDSPT.addItem("--Danh sách phòng thi--");
        ArrayList<PhongThi_DTO> list = pb.getDSPhongThi(idkt);
        for(PhongThi_DTO pt : list){
            cbDSPT.addItem(pt.getIDPhongThi()+":"+pt.getTenPhongThi());
        }
    }
    public void setComboKhoaThi() {
        
        try {
            ArrayList<KhoaThi_DTO> list = kb.getAll();
            for (KhoaThi_DTO k : list) {
                cbKhoaThi.addItem(k.getIDKhoaThi() + ":" + k.getTenKhoaThi());
                
            }
        } catch (SQLException ex) {
            Logger.getLogger(PhongThi.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
