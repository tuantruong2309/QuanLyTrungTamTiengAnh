    LocalDate NgaySinh;

    public ThiSinh_DTO() {
    }

    public int getIDThiSinh() {
        return IDThiSinh;
    }

    public void setIDThiSinh(int IDThiSinh) {
        this.IDThiSinh = IDThiSinh;
    }

    public String getHo() {
        return Ho;
    }

